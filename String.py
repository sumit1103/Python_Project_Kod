# Create a string
str = "Python"

# Print the string and its length
print("String:", str)
print("Length:", len(str))

# Access characters using positive indexing
print("str[0] =", str[0])   # P
print("str[3] =", str[3])   # h
print("str[5] =", str[5])   # n

# Access characters using negative indexing
print("str[-1] =", str[-1])  # n
print("str[-4] =", str[-4])  # t